    private void limpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) this.tabla_reportes.getModel();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        
    }
    
