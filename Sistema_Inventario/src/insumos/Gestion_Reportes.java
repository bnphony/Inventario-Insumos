    public void imprimirProductos(JTable tabla, String condicion){
        
        tabla.setDefaultRenderer(Object.class, new Render());
        DefaultTableModel modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        
        t_ingreso = 0;
        t_retiro = 0;
        t_costo = 0;
        
        modelo.addColumn("Codigo");
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        modelo.addColumn("Retiros");
        modelo.addColumn("Ingresos");
        modelo.addColumn("Total");
        
        if(combo_productos.getSelectedIndex() != 0){
            modelo.addColumn("Fecha");
        }
        
       
        
        Object[] fila = new Object[modelo.getColumnCount()];
        
        ArrayList<ReporteInsumo> insumos = new ArrayList<ReporteInsumo>();
        ReporteInsumo in = new ReporteInsumo();
        insumos = MainReportes.consultarProductos2(condicion);
        for(int i = 0; i < insumos.size(); i++){
            in = insumos.get(i);
            fila[0] = in.getId_insumo();
            fila[1] = in.getNombre();
            fila[2] = in.getCantidad();
            fila[3] = in.getPrecio_unitario();  
            BigDecimal retiro = new BigDecimal(in.getRetiros());
            fila[4] = retiro.setScale(2, RoundingMode.HALF_UP);
            System.out.println("fila retiros: " + fila[4].toString());
            BigDecimal ingreso = new BigDecimal(in.getIngresos());
            fila[5] = ingreso.setScale(2, RoundingMode.HALF_UP);
            BigDecimal total = new BigDecimal(in.getTotal());
            fila[6] = total.setScale(2, RoundingMode.HALF_UP);
            if(combo_productos.getSelectedIndex() != 0){
                fila[7] = in.getFecha();
            }
        
            modelo.addRow(fila);
            
            tabla.setModel(modelo);
            tabla.setRowHeight(30);
            
            
            
            t_ingreso += in.getIngresos();
            t_retiro += in.getRetiros();
            t_costo += in.getTotal();
        }
        
        rinsumos = insumos;
        if(insumos.size() == 0){
            limpiarTabla();
        }
        
        if(combo_productos.getSelectedIndex() == 0){
            periodo_insumo = "";
            g.imprimirGrafico(tabla_reportes, 6, "Costo Total", 0);
        }else{
            periodo_insumo = "Activado";
            g.imprimirGrafico(tabla_reportes, 6, "Costo Total", 7);
            
        }
        
        
        this.total_ingreso.setText(String.format("%.2f", t_ingreso));
        this.total_retiro.setText(String.format("%.2f", t_retiro));
        this.total_costo.setText("$ " + String.format("%.2f", t_costo));
        
      
        alinearTabla();
    }
    
