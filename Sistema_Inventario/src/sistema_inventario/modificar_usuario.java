    public void eliminar() {
        t.iniciar1();
        Conexion cc = new Conexion();
        Connection con = cc.getConnection();
        boolean u = false;
        String nombre = this.jtextusuario1.getText().toString();
        
        try {
            con = cc.getConnection();
            
            
            pat = con.prepareStatement("UPDATE usuario SET estado_usuario = 0 "
                    + "WHERE nombre_usuario = ?");
            pat.setString(1, nombre);

            int res = pat.executeUpdate();
            
            t.pausar("Eliminar");
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Persona Eliminada");
                limpiarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "No existe el Usuario que desea Eliminar");
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error tipo : " + e.getMessage());
        }

    }

    public void Actualizar() {
        t.iniciar1();
        Conexion cc = new Conexion();
        Connection con = cc.getConnection();
        String contrase単a_usuario = String.valueOf(jtextcontra.getPassword());
        //Connection con=null;
        String nombre = this.jtextusuario1.getText().toString();
        boolean u = false;
        try {
            con = cc.getConnection();
            
            u = MainInicio.existeUsuario(nombre);
            
            if (u) {
                pat = con.prepareStatement("UPDATE usuario SET apellido_usuario=?,"
                        + "password_usuario=?,correo_usuario=?,tipo_usuario=? WHERE nombre_usuario=?");               
                pat.setString(1, jtextapellido.getText());
                pat.setString(2, contrase単a_usuario);
                pat.setString(3, jtextcorreo.getText());
                pat.setString(4, combo_tipo.getSelectedItem().toString());
                pat.setString(5, jtextusuario1.getText());

                int res = pat.executeUpdate();
                
                t.pausar("Actualizar");
                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "Persona Modificada");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al Modificar");
                }

            }else{
                JOptionPane.showMessageDialog(null, "<html>"+"No se puede actualizar el Nombre de Usuario <strong>[ "+ nombre +" ]</strong>, <br>"
                        + " debido a que es el identificador del registro "+"</html>", "Usuario No Registrado", JOptionPane.ERROR_MESSAGE);
            }
            
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error tipo : " + e.getMessage());
        }

    }

