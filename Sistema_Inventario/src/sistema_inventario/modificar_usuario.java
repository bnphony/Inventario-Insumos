    public void consulta(String nombre) {
        t.iniciar1();
        Conexion cc = new Conexion();
        Connection con = cc.getConnection();
        
        try {
            con = cc.getConnection();           
            
            
            pat = con.prepareStatement("SELECT *FROM usuario WHERE nombre_usuario=?");
            pat.setString(1, nombre);
            ResultSet a = pat.executeQuery();

            if (a.next()) {
                jtextusuario.setText(a.getString("nombre_usuario"));
                jtextusuario1.setText(a.getString("nombre_usuario"));
                jtextapellido.setText(a.getString("apellido_usuario"));
                jtextcontra.setText(a.getString("password_usuario"));
                jtextcorreo.setText(a.getString("correo_usuario"));
                combo_tipo.setSelectedItem(a.getString("tipo_usuario"));
                
                t.pausar("Consulta");
            } else {
                JOptionPane.showMessageDialog(null, "No existe ese nombre, Ingrese el Nombre de algun Usuario");
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al buscar");
        }
    }

