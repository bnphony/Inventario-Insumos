    public void imprimirEliminados(){
        t.iniciar1();
        Conexion cc = new Conexion();
        Connection con = cc.getConnection();            
        String[] titulos = {" Nombre ", "  Tipo "};
        String[] registros = new String[2];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        String SQL = "SELECT * FROM usuario WHERE estado_usuario = 0";

        try {

            st = con.createStatement();
            rs = st.executeQuery(SQL);

            while (rs.next()) {
                registros[0] = rs.getString("nombre_usuario");
                registros[1] = rs.getString("tipo_usuario");

                modelo.addRow(registros);
            }
            tabla_ueliminados.setModel(modelo);
            tabla_ueliminados.setRowHeight(30);
            con.close();
            t.pausar("Imprimir Eliminados");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de acceso " + e.getMessage());
        }
    }
    
    
    private void reactivarUsuario(){
        t.iniciar1();
        Conexion cc = new Conexion();
        Connection con = cc.getConnection();
        
        try {
            con = cc.getConnection();
            pat = con.prepareStatement("UPDATE usuario SET estado_usuario = 1 "
                    + "WHERE nombre_usuario = ?");
            pat.setString(1, nombre);

            int res = pat.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Se Reactivo el Usuario");
            } else {
                JOptionPane.showMessageDialog(null, "Error al Recuperar al Usuario");
            }
            con.close();
            t.pausar("Reactivar Usuario");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error tipo : " + e.getMessage());
        }
    }
